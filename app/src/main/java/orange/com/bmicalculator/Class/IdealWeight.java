package orange.com.bmicalculator.Class;


public class IdealWeight {

    public String percentagePI;
    public String leanBodyMass;
    public String obesity;
    public String idealWeight;

    public String getObesity() {
        return obesity;
    }

    public void setObesity(String obesity) {
        this.obesity = obesity;
    }


    public String getPercentagePI() {
        return percentagePI;
    }

    public void setPercentagePI(String percentagePI) {
        this.percentagePI = percentagePI;
    }

    public String getLeanBodyMass() {
        return leanBodyMass;
    }

    public void setLeanBodyMass(String leanBodyMass) {
        this.leanBodyMass = leanBodyMass;
    }


    public String getIdealWeight() {
        return idealWeight;
    }

    public void setIdealWeight(String idealWeight) {
        this.idealWeight = idealWeight;
    }




}
